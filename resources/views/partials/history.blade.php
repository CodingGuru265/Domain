    <script>
    // CONTINUOUS AUTOSCROLL CAROUSEL
    document.addEventListener('DOMContentLoaded', function () {
        const track = document.getElementById('carouselTrack');
        const items = track.querySelectorAll('.carousel-item');
        const itemCount = items.length / 2; // because we duplicated
        let itemWidth = items[0].offsetWidth + parseInt(getComputedStyle(items[0]).marginLeft) + parseInt(getComputedStyle(items[0]).marginRight);
        let scrollAmount = 0;
        let animationFrame;

        function animate() {
            scrollAmount += 1; // px per frame, adjust for speed
            if (scrollAmount >= itemWidth * itemCount) {
                scrollAmount = 0;
            }
            track.style.transform = `translateX(-${scrollAmount}px)`;
            animationFrame = requestAnimationFrame(animate);
        }

        // Responsive: recalculate itemWidth on resize
        window.addEventListener('resize', () => {
            itemWidth = items[0].offsetWidth + parseInt(getComputedStyle(items[0]).marginLeft) + parseInt(getComputedStyle(items[0]).marginRight);
        });

        animate();
    });
